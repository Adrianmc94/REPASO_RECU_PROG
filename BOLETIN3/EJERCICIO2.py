numero1 = int(input("Intruduce el primer numero: "))
numero2 = int(input("Intruduce el segundo numero: "))

if numero1 > numero2 or numero1 == numero2:
     resultado = numero1 - numero2
     print(f"La resta entre el numero {numero1} y el numero {numero2} es {resultado}")
else:
    resultado2  = numero1 + numero2
    print(f"La suma es {resultado2}")
